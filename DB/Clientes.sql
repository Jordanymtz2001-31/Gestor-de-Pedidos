--TABLA CLIENTE
CREATE TABLE CLIENTE(
    ID_CLIENTE NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    NOMBRE NVARCHAR2(30) NOT NULL,
    EMAIL NVARCHAR2(30)UNIQUE NOT NULL,
    TELEFONO NVARCHAR2(20) UNIQUE NOT NULL,
    ESTATUS NVARCHAR2(30)DEFAULT 'USER' CHECK (ESTATUS IN ('USER', 'ADMIN')) --AQUI POR DEFECTO ESTARA ACTIVADO Y PUEDE SER INACTIVO
);

